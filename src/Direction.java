
/**
 * Each direction corresponds to compass vector.
 * 
 * @author Josh Gillham
 * @version 10-29-12
 */
public enum Direction {
    /** Up. */
    North, 
    /** North East. */
    NorthEast,
    /** Right. */
    East,
    /** South East. */
    SouthEast,
    /** Down. */
    South, 
    /** South West. */
    SouthWest,
    /** Left. */
    West,
    /** North West. */
    NorthWest
}
